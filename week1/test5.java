public class test5 {
    public static void main(String[] args) {
        System.out.println( true );
 System.out.println( false );
 System.out.println( 'A' );
 System.out.println( '\101' );
 System.out.println( '\u0041' ); //Unicode Escape Sequence	Represents a character using its Unicode value.
 System.out.println( "Code\tName" );
 System.out.println( "Line1\r\nLine2" );
 System.out.println( "Page 1\fPage 2" );
/* 
 \t	Tab	Moves the cursor to the next tab stop.
\r	Carriage Return	Moves the cursor to the beginning of the line.
\n	Line Feed	Moves the cursor to a new line.
\f	Form Feed	Moves to the next page in text output.
\101	Octal Escape Sequence	Represents a character using its octal value. */
    }
}
